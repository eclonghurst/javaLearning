package org.example;

public class Calculator {

    public static float CalcAdd(float numOne, float numTwo) {
        return numOne + numTwo;

    }

    public static float CalcSubtract(float numOne, float numTwo){
        return numOne - numTwo;
    }

    public static float CalcDivide(float numOne, float numTwo){
        return numOne / numTwo;
    }

    public static float CalcMultiply(float numOne, float numTwo){
        return numOne * numTwo;
    }

    public static float CalcMod(float numOne, float numTwo){
        return numOne % numTwo;
    }
}
